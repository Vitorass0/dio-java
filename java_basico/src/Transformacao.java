public class Transformacao {
   //O método Integer.valueOf() é utilizado para converter 
   // um texto em um número inteiro.
        public static void main(String[] args) {
            String numeroEmTexto = "123";
            Integer numero = Integer.valueOf(numeroEmTexto);
            
            System.out.println("Número: " + numero);
            System.out.println("Texto: " + numeroEmTexto);
        }
    
}
